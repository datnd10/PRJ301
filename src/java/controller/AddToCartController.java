    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setHeader("Cache-Control", "no-cache, no-store, must-revalidate");
        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("account");
        if (user == null) {
            response.sendRedirect("login");
            return;
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        ProductColorDao dao = new ProductColorDao();
        CartDao cartdao = new CartDao();
        String quantity_raw = request.getParameter("quantity");
        String color = request.getParameter("color");
        String productIDraw = request.getParameter("productID");
        User user = (User) session.getAttribute("account");
        String inventory_raw = request.getParameter("inventory");
        if(Integer.parseInt(inventory_raw) == 0) {
            response.getWriter().print("SoldOut");
            return;
        }
        try {
            int productId = Integer.parseInt(productIDraw);
            int quantity = Integer.parseInt(quantity_raw);
            ProductColor pc = dao.getProductColorByColorAndProductID(productId, color);
            Cart cartCheck = cartdao.checkItemCart(user.getUser_id(), pc.getProduct_color_id());
            if (cartCheck == null) {
                cartdao.addToCart(user.getUser_id(), pc.getProduct_color_id(), quantity);
            } else {
                cartdao.updateCartQuantity(user.getUser_id(), pc.getProduct_color_id(), quantity + cartCheck.getQuantity());
            }
        } catch (Exception e) {
        }
    }

